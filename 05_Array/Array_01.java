public class Array_01 {
    public static void main(String[] args) {
        int nums[] = new int[4];

        nums[0] = 6; // chnage in value
        nums[1] = 9; 
        nums[2] = 2; 
        nums[3] = 7; 

        for(int i = 0; i <= 3; i++) {
            System.out.println(nums[i]);
        }
        
    }
}